// creating router links for each menu
routes.forEach((x: any) => {
  x.link = x.name
    .toLowerCase()
    .replace(/[^a-zA-Z0-9 ]/g, '')
    .replace(/ /g, '-');

  x.routes.forEach((z: any) => {
    z.link = z.name
      .toLowerCase()
      .replace(/[^a-zA-Z0-9 ]/g, '')
      .replace(/ /g, '-');

    z.routes.forEach((c: any) => {
      c.link = c.title
        .toLowerCase()
        .replace(/[^a-zA-Z0-9 ]/g, '')
        .replace(/ /g, '-');
    });
  });
});
